/** Понятное сообщение пользователю при сбое Gemini (детали остаются в логах). */
function buildAuditGeminiErrorResponse(msg: string): Response {
  const m = msg;

  if (/HTTP 429|RESOURCE_EXHAUSTED|Too Many Requests/i.test(m)) {
    return new Response(JSON.stringify({ error: "Слишком много запросов. Попробуй чуть позже." }), {
      status: 429,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }

  const aiOverloaded =
    /503|"UNAVAILABLE"|high demand|The model is overloaded|Service Unavailable|DEADLINE_EXCEEDED|EAI_/i.test(m);
  if (aiOverloaded) {
    return new Response(
      JSON.stringify({
        error:
          "Сервис AI временно перегружен или недоступен. Подождите 1–2 минуты и запустите проверку снова.",
      }),
      { status: 503, headers: { ...corsHeaders, "Content-Type": "application/json" } },
    );
  }

  if (/HTTP 400|INVALID_ARGUMENT|Request payload size exceeds|maximum|too large|Payload too large/i.test(m)) {
    return new Response(
      JSON.stringify({
        error:
          "Страница слишком «тяжёлая» для разбора за один запрос (очень длинная страница или большой скриншот). Попробуйте главную страницу сайта, более короткий URL или повторите через пару минут.",
      }),
      { status: 413, headers: { ...corsHeaders, "Content-Type": "application/json" } },
    );
  }

  if (
    /safety|blocked|BLOCK_REASON|HARM_CATEGORY|PROHIBITED_CONTENT|Responsible AI|RECITATION|cannot answer about|policy/i.test(
      m,
    )
  ) {
    return new Response(
      JSON.stringify({
        error:
          "Модель не может завершить разбор этого URL (фильтры безопасности или политика контента). Попробуйте другую страницу того же сайта или главную.",
      }),
      { status: 422, headers: { ...corsHeaders, "Content-Type": "application/json" } },
    );
  }

  if (/MAX_TOKENS|обрезан|truncat/i.test(m)) {
    return new Response(
      JSON.stringify({
        error:
          "Отчёт не поместился в один ответ модели (слишком большой объём). Попробуйте главную страницу сайта или запустите проверку ещё раз.",
      }),
      { status: 502, headers: { ...corsHeaders, "Content-Type": "application/json" } },
    );
  }

  if (/не удалось распарсить JSON|нет вызова return_audit|пустой JSON в ответе|Unexpected token/i.test(m)) {
    return new Response(
      JSON.stringify({
        error:
          "Не удалось собрать отчёт из ответа AI (временный сбой или неполный ответ). Запустите проверку ещё раз через минуту.",
      }),
      { status: 502, headers: { ...corsHeaders, "Content-Type": "application/json" } },
    );
  }

  if (/HTTP 401|HTTP 403|PERMISSION_DENIED|API key not valid|API_KEY_INVALID/i.test(m)) {
    return new Response(
      JSON.stringify({
        error:
          "Временная ошибка сервиса анализа. Попробуйте позже или напишите в поддержку, если повторяется.",
      }),
      { status: 502, headers: { ...corsHeaders, "Content-Type": "application/json" } },
    );
  }

  if (m.length <= 20) {
    return new Response(
      JSON.stringify({
        error: "Ошибка Google Gemini при разборе сайта. Проверьте ключ API и лимиты.",
      }),
      { status: 502, headers: { ...corsHeaders, "Content-Type": "application/json" } },
    );
  }

  return new Response(
    JSON.stringify({
      error:
        "Не удалось завершить разбор сайта. Попробуйте ещё раз через минуту, другую страницу или главную сайта.",
    }),
    { status: 502, headers: { ...corsHeaders, "Content-Type": "application/json" } },
  );
}

async function resolveUserId(req: Request): Promise<string | null> {
  const supabaseUrl = Deno.env.get("SUPABASE_URL");
  const anon = Deno.env.get("SUPABASE_ANON_KEY");
  if (!supabaseUrl || !anon) return null;
  const auth = req.headers.get("Authorization") || "";
  const token = auth.replace(/^Bearer\s+/i, "").trim();
  if (!token || token === anon) return null;
  const userClient = createClient(supabaseUrl, anon, {
    global: { headers: { Authorization: `Bearer ${token}` } },
  });
  const { data: { user } } = await userClient.auth.getUser();
  return user?.id ?? null;
}

function extractStructuredContent(html: string): string {
  const cleaned = html
    .replace(/<script[\s\S]*?<\/script>/gi, " ")
    .replace(/<style[\s\S]*?<\/style>/gi, " ")
    .replace(/<noscript[\s\S]*?<\/noscript>/gi, " ");

  const pick = (re: RegExp, max = 30) => {
    const out: string[] = [];
    let m: RegExpExecArray | null;
    while ((m = re.exec(cleaned)) && out.length < max) {
      const t = (m[1] || "").replace(/<[^>]+>/g, " ").replace(/\s+/g, " ").trim();
      if (t) out.push(t);
    }
    return out;
  };

  const h1 = pick(/<h1[^>]*>([\s\S]*?)<\/h1>/gi, 5);
  const h2 = pick(/<h2[^>]*>([\s\S]*?)<\/h2>/gi, 30);
  const h3 = pick(/<h3[^>]*>([\s\S]*?)<\/h3>/gi, 50);
  const buttons = pick(/<button[^>]*>([\s\S]*?)<\/button>/gi, 30);
  const links = pick(/<a[^>]*>([\s\S]*?)<\/a>/gi, 50);

  const videoHosts = [
    "youtube.com","youtu.be","youtube-nocookie.com",
    "vk.com/video","vk.ru/video","vkvideo",
    "rutube.ru","kinescope.io","vimeo.com",
    "dzen.ru/video","ok.ru/video","boomstream","wistia","loom.com","cdnvideo",
  ];
  const iframeSrcs = pick(/<iframe[^>]*src=["']([^"']+)["'][^>]*>/gi, 50);
  const videoTags = (cleaned.match(/<video[\s>]/gi) || []).length;
  const sourceTags = pick(/<source[^>]*src=["']([^"']+\.(?:mp4|webm|m3u8|mov))["'][^>]*>/gi, 30);
  const videoLinks = pick(
    /<a[^>]*href=["']([^"']*(?:youtube|youtu\.be|vk\.com\/video|vkvideo|rutube|kinescope|vimeo|dzen\.ru\/video|ok\.ru\/video)[^"']*)["'][^>]*>/gi,
    30,
  );

  const mediaSignals: string[] = [];
  if (videoTags) mediaSignals.push(`<video> тегов: ${videoTags}`);
  if (sourceTags.length) mediaSignals.push(`видео-источники: ${sourceTags.slice(0,5).join(", ")}`);
  const videoIframes = iframeSrcs.filter((s) => videoHosts.some((h) => s.toLowerCase().includes(h)));
  if (videoIframes.length) mediaSignals.push(`видео-iframes (${videoIframes.length}): ${videoIframes.slice(0,5).join(", ")}`);
  if (videoLinks.length) mediaSignals.push(`ссылки на видео (${videoLinks.length}): ${videoLinks.slice(0,5).join(", ")}`);

  const imgAlts = pick(/<img[^>]*alt=["']([^"']{3,})["'][^>]*>/gi, 60);
  const reviewAlts = imgAlts.filter((a) => /отзыв|review|testimonial|клиент|кейс|case/i.test(a));

  const plain = cleaned.replace(/<[^>]+>/g," ").replace(/\s+/g," ").trim().slice(0, 12000);

  return [
    `H1: ${h1.join(" | ") || "—"}`,
    `H2 (${h2.length}): ${h2.join(" | ") || "—"}`,
    `H3 (${h3.length}): ${h3.slice(0,30).join(" | ") || "—"}`,
    `Кнопки/CTA (${buttons.length}): ${buttons.slice(0,20).join(" | ") || "—"}`,
    `Ссылки (${links.length}, первые 20): ${links.slice(0,20).join(" | ") || "—"}`,
    `МЕДИА-СИГНАЛЫ: ${mediaSignals.length ? mediaSignals.join(" ; ") : "не обнаружено"}`,
    `IMG alt с упоминанием отзывов (${reviewAlts.length}): ${reviewAlts.slice(0,10).join(" | ") || "—"}`,
    ``,
    `ТЕКСТ СТРАНИЦЫ:`,
    plain,
  ].join("\n");
}

