export function roleConfig(role: IdeaLabCoachRole): IdeaLabRoleConfig {
  return IDEA_LAB_ROLES.find((r) => r.id === role) ?? IDEA_LAB_ROLES[0];
}

export function normalizeCoachRole(raw: unknown): IdeaLabCoachRole {
  if (raw === "investor" || raw === "critic" || raw === "coach") return raw;
  return "coach";
}

